public class Calculator {

	/**
	 * 加法
	 * 
	 * @param arg1
	 * @param arg2
	 * @return
	 */
	public int add(int arg1, int arg2) {
		return arg1 + arg2;
	}

	/**
	 * 减法
	 * 
	 * @param arg1
	 * @param arg2
	 * @return
	 */
	public int substract(int arg1, int arg2) {
		return arg1 - arg2;
	}

	/**
	 * 乘法
	 * 
	 * @param arg1
	 * @param arg2
	 * @return
	 */
	public int multiply(int arg1, int arg2) {
		return arg1 * arg2;
	}

	/**
	 * 除法
	 */
	public int divide(int arg1, int arg2) {
		return arg1 / arg2;
	}
}
